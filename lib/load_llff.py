def spherify_poses(poses, bds, depths):

    
    # Get centers of every pose
    new_poses_centers = poses[:,:3,3]

    # Get the mean center 
    mean_center = np.mean(new_poses_centers, axis=0, keepdims=True) 
     
    # Of these poses, get the norms of all these centers and get the max norm, from the distance to the mean center
    norms_centers = np.linalg.norm(new_poses_centers - mean_center, axis=1)
    biggest_norm = np.max(norms_centers)

    # Finally, just normalize the centers of the poses with the max norm. 
    poses[:,:3,3] = (poses[:,:3,3] - mean_center  )/ (biggest_norm*1.1)

    depths /= (biggest_norm*1.1)
    bds /= (biggest_norm*1.1)


    return poses, poses, bds, depths


